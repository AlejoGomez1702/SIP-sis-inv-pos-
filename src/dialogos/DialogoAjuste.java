    /**
     * Inicializa el titulo de esta vetana según donde se le llame.
     * @param nueva Si se intenta crear una nueva categoria, marca o unidad de medida.
     * @param ajuste Indica desde donde fue llamada la ventana categoria, marca o unidad de medida.
     */
    public void initDialog(boolean nueva, char ajuste)
    {        
        switch(ajuste)
        {
            //Categoría.
            case 'c':
                if(nueva)
                    this.txtEncabezado.setText("Agregar Nueva Categoría");
                else
                    this.txtEncabezado.setText("Modificar Categoría");
                break;

            //Marca.
            case 'm':
                if(nueva)
                    this.txtEncabezado.setText("Agregar Nueva Marca");
                else
                    this.txtEncabezado.setText("Modificar Marca");
                break;

            //Unidad de medida.
            case 'u':
                if(nueva)
                    this.txtEncabezado.setText("Agregar Unidad De Medida");
                else
                    this.txtEncabezado.setText("Modificar Unidad De Medida");
                break;
        }        
    }

