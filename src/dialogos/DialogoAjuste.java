    private void btnAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAceptarMouseClicked
        String name = this.txtNombre.getText();
        String desc = this.txtDescripcion.getText();
        
        if(nombre.equals(""))
            JOptionPane.showMessageDialog(this, "No Asignó Un Nombre, Compruebe Los Datos");
        else
        {
            switch(this.ajuste)
            {
                //Categoria.
                case 'c':
                    this.categoria = new Categoria(-1, name, desc);
                    break;
                
                //Marca.
                case 'm':
                    this.marca = new Marca(-1, name, desc);
                    break;
                    
                //Unidad de medida.
                case 'u':
                    this.unidadMedida = new UnidadMedida(-1, name, desc);
                    break;
            }
            this.dispose();
        }        
    }//GEN-LAST:event_btnAceptarMouseClicked

