    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String txtLeido = this.txtLectura.getText();
        String txtRefer = this.txtReferencia.getText();
        if(txtLeido.equals("") || txtRefer.equals(""))
            JOptionPane.showMessageDialog(this, "Por Favor Registre Todos Los Campos");
        else
        {
            //Registra en la base de datos y el sistema.
            this.tequila.getMap().put(txtLeido, this.elementRegister);
            boolean ban = this.tequila.getBd().getCrudScanner()
                    .createMap(txtLeido, this.elementRegister);
            if(ban)
            {
                JOptionPane.showMessageDialog(this, "Código De Barras Registrado Correctamente");
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "No Fue Posible Registrar El Código De Barras");
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

