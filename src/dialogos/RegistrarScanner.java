    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        ListaProductos lp = new ListaProductos(null, true, this.tequila, 's', false);
        lp.setVisible(true);
        Elemento prod = lp.getComponente();
        if(prod != null)
        {
            this.elementRegister = prod;
            String descrip = prod.getProducto().getNombre() + " " + prod.getMarca()
                    + " X " + prod.getUnidadMedida();
            this.txtReferencia.setText(descrip);
        }
    }//GEN-LAST:event_btnSeleccionarActionPerformed

